import Head from 'next/head'
import styles from '../styles/Home.module.css'
import react, { useState } from 'react';

export default function Home() {
  const [searchResult, setSearchResult] = useState([])
  const [isSearching, setSisSearching] = useState(false)

  const query = async () => {
    setSisSearching(true);
    const inputQuery = document.querySelector('#inputQuery') as HTMLInputElement;
    if (!inputQuery) {
      setSisSearching(false);
      return
    }
    const ENDPOINT = `https://api.baserow.io/api/database/rows/table/71169/?user_field_names=true&search=${inputQuery.value}`;
    let response = await fetch(ENDPOINT, {
      headers: {
        Authorization: 'Token z8XXWteBKfhsyTKxwMmFcg7cEVswU7pt'
      }
    });

    response = await response.json();
    setSearchResult(response['results']);
    setSisSearching(false);
  }

  const handleSearchEnter = (event) => {
    if (event.keyCode === 13) {
      query()
    }
  }

  return (
    <div className={styles.container}>
      <Head>
        <title>Kamus Hukum</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <div className="flex w-full bg-slate-200 h-screen justify-center">
          <div className='max-w-lg w-full'>
            <h1 className='pt-10 h-auto text-center text-3xl text-slate-600 w-full align-center font-bold'>Kamus Hukum Indonesia</h1>
            <div className='mt-10 mx-2 relative'>
              <input type="text" className="h-14 w-full pl-5 pr-20 rounded-lg z-0 focus:shadow focus:outline-none shadow-lg" id="inputQuery" placeholder="Cari istilah hukum di sini..." onKeyDown={(e) => handleSearchEnter(e)} />
              <div className="absolute top-2 right-2">
                <button className="h-10 w-20 text-white rounded-lg bg-blue-500 hover:bg-red-600" onClick={() => query()}>Cari</button>
              </div>
            </div>

            <div className="w-full flex mt-4 px-2">
              <div className="w-full block rounded-lg shadow-lg bg-white text-left ">
                {searchResult && searchResult.length > 0 && (
                  <div className="py-3 px-6 border-b border-gray-300 font-medium">
                    Hasil Pencarian
                  </div>
                )
                }

                {
                  isSearching && (
                    <div className="w-full p-6 border-b border-gray-300">
                      <p className="animate-pulse text-gray-700 text-base">
                        <div className="w-full bg-gray-300 h-4 rounded-md mb-2"></div>
                        <div className="w-full bg-gray-300 h-4 rounded-md mb-2"></div>
                        <div className="w-full bg-gray-300 h-4 rounded-md mb-2"></div>
                        <div className='w-full flex justify-end mt-5'>
                          <div className="w-10 mx-1 bg-gray-300 h-6 rounded-md mb-2"></div>
                          <div className="w-10 mx-1 bg-gray-300 h-6 rounded-md mb-2"></div>
                          <div className="w-10 mx-1 bg-gray-300 h-6 rounded-md mb-2"></div>
                        </div>
                      </p>
                    </div>
                  )

                }
                {searchResult.map(item => {
                  return (
                    <div className="w-full p-6 border-b border-gray-300" key={item.id}>
                      <p className="text-gray-700 text-base mb-4">
                        <b>{item.alias}</b> : {item.definition}
                      </p>
                      <div className='w-full flex justify-end'>
                        <button type="button" className="inline-block p-2 bg-slate-400 text-white font-medium text-xs leading-tight uppercase rounded shadow-md hover:bg-slate-700 hover:shadow-lg">Salin</button>
                        <button type="button" className="inline-block mx-2 p-2 bg-slate-400 text-white font-medium text-xs leading-tight uppercase rounded shadow-md hover:bg-slate-700 hover:shadow-lg">Bookmark</button>
                        <button type="button" className="inline-block p-2 bg-slate-400 text-white font-medium text-xs leading-tight uppercase rounded shadow-md hover:bg-slate-700 hover:shadow-lg">Source</button>
                      </div>
                    </div>

                  )
                })}
              </div>
            </div>
          </div>
        </div>
      </main >


    </div >
  )
}
